public class Obiect {
    int hp;
    int attack;
    int specialAttack;
    int defense;
    int specialDefense;

    Obiect(int hp, int attack, int specialAttack, int defense, int specialDefense) {
        this.hp = hp;
        this.attack = attack;
        this.specialAttack = specialAttack;
        this.defense = defense;
        this.specialDefense = specialDefense;
    }
}
